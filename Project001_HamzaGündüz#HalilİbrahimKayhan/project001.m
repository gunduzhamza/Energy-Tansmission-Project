function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


% --- Executes during object creation, after setting all properties.
function uibuttongroup1_CreateFcn(hObject, eventdata, handles)
% hObject    handle to uibuttongroup1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    empty - handles not created until after all CreateFcns called


% --- Executes when selected object is changed in uibuttongroup1.
function uibuttongroup1_SelectionChangedFcn(hObject, eventdata, handles)
% hObject    handle to the selected object in uibuttongroup1 
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
S=str2double(get(handles.edit1,'String'));
Vll=str2double(get(handles.edit2,'String'));

S=S/2;
i=(S*10^6)/(sqrt(3)*(Vll*10^3));
set(handles.edit19,'String',i);
a=str2double(get(handles.edit4,'String'));
b=str2double(get(handles.edit5,'String'));
c=str2double(get(handles.edit6,'String'));
mw=str2double(get(handles.edit3,'String'));
ew=str2double(get(handles.editEw,'String'));
Ra=sqrt((a^2)+(mw^2))*0.3048;
Rb=sqrt((b^2)+(mw^2))*0.3048;
Rc=sqrt((c^2)+(mw^2))*0.3048;
Ra2=sqrt((a^2)+(ew^2))*0.3048;
Rb2=sqrt((b^2)+(ew^2))*0.3048;
Rc2=sqrt((c^2)+(ew^2))*0.3048;
Ra3=sqrt((a^2)+(ew+(mw*2))^2)*0.3048;
Rb3=sqrt((b^2)+(ew+(mw*2))^2)*0.3048;
Rc3=sqrt((c^2)+(ew+(mw*2))^2)*0.3048;

switch get(eventdata.NewValue,'Tag')
    case 'middlerow'
        Hax_m=(i*cos(deg2rad(0)))/(2*pi*Ra);
        Hay_m=(i*sin(deg2rad(0)))/(2*pi*Ra);
        Hbx_m=(i*cos(deg2rad(-120)))/(2*pi*Rb);
        Hby_m=(i*sin(deg2rad(-120)))/(2*pi*Rb);
        Hcx_m=(i*cos(deg2rad(-240)))/(2*pi*Rc);
        Hcy_m=(i*sin(deg2rad(-240)))/(2*pi*Rc);
        totalX1 = 2*(Hax_m+Hbx_m+Hcx_m);
        totalY1 = 2*(Hay_m+Hby_m+Hcy_m);
        TotalH1=sqrt(totalY1^2+totalX1^2);
        u=(4*pi)/(10);
        density1=u*TotalH1;

        set(handles.edit7,'String',Ra);
        set(handles.edit8,'String',Rb);
        set(handles.edit9,'String',Rc);
        set(handles.edit14,'String',Ra);
        set(handles.edit15,'String',Rb);
        set(handles.edit16,'String',Rc);
        set(handles.edit_int,'String',TotalH1);
        set(handles.edit18,'String',density1*10);
        
    case 'edgerow'
        Hax=(i*cos(deg2rad(0)))/(2*pi*Ra2);
        Hay=(i*sin(deg2rad(0)))/(2*pi*Ra2);
        Hbx=(i*cos(deg2rad(-120)))/(2*pi*Rb2);
        Hby=(i*sin(deg2rad(-120)))/(2*pi*Rb2);
        Hcx=(i*cos(deg2rad(-240)))/(2*pi*Rc2);
        Hcy=(i*sin(deg2rad(-240)))/(2*pi*Rc2);
        Hax2=(i*cos(deg2rad(0)))/(2*pi*Ra3);
        Hay2=(i*sin(deg2rad(0)))/(2*pi*Ra3);
        Hbx2=(i*cos(deg2rad(-120)))/(2*pi*Rb3);
        Hby2=(i*sin(deg2rad(-120)))/(2*pi*Rb3);
        Hcx2=(i*cos(deg2rad(-240)))/(2*pi*Rc3);
        Hcy2=(i*sin(deg2rad(-240)))/(2*pi*Rc3);

        totalX = Hax+Hbx+Hcx+Hax2+Hbx2+Hcx2;
        totalY = Hay+Hby+Hcy+Hay2+Hby2+Hcy2;
        TotalH=sqrt(totalY^2+totalX^2);
        u=(4*pi)/(10);
        density=u*TotalH;

        set(handles.edit7,'String',Ra2);
        set(handles.edit8,'String',Rb2);
        set(handles.edit9,'String',Rc2);
        set(handles.edit14,'String',Ra3);
        set(handles.edit15,'String',Rb3);
        set(handles.edit16,'String',Rc3);
        set(handles.edit_int,'String',TotalH);
        set(handles.edit18,'String',density*10);
        
end



